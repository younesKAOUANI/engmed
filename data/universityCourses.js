// Helper function to get courses by filters
export function getCoursesByFilters(studyYear, courseYear) {
  if (!studyYear || !courseYear) return [];
  return universityCourses[studyYear]?.[courseYear] || [];
}

// Get all courses for a study year
export function getCoursesByStudyYear(studyYear) {
  if (!studyYear) return [];
  const yearData = universityCourses[studyYear] || {};
  return Object.values(yearData).flat();
}

// Get course count by study year
export function getCourseCount(studyYear) {
  const courses = getCoursesByStudyYear(studyYear);
  return courses.length;
}
